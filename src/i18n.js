export let lang = "zh";

export function t(key) {
  const val = STRINGS[lang]?.[key];
  if (val !== undefined) return val;
  return STRINGS.zh[key] !== undefined ? STRINGS.zh[key] : key;
}

export function setLang(newLang) {
  if (newLang === "zh" || newLang === "en") {
    lang = newLang;
  }
}
